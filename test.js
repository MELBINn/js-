// Using Math.pow
function squareUsingMathPow(number) {
  return Math.pow(number, 2);
}

// Example usage
const result1 = squareUsingMathPow(5);
console.log(result1); // Output: 25


// Using the exponentiation operator
function squareUsingExponentiation(number) {
  return number ** 2;
}

// Example usage
const result2 = squareUsingExponentiation(5);
console.log(result2); // Output: 25

